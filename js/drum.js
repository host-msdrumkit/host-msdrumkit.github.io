function togglefoam() {
  var x = document.getElementById("foaminfo");
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }

  if (document.getElementById("corkinfo").style.display === "block"){
    document.getElementById("corkinfo").style.display = "none";
  }

  if (document.getElementById("woodinfo").style.display === "block"){
    document.getElementById("woodinfo").style.display = "none";
  }

  if (document.getElementById("pvcinfo").style.display === "block"){
    document.getElementById("pvcinfo").style.display = "none";
  }

  if (document.getElementById("elecinfo").style.display === "block"){
    document.getElementById("elecinfo").style.display = "none";
  }

  if (document.getElementById("pinfo").style.display === "block"){
    document.getElementById("pinfo").style.display = "none";
  }
}


